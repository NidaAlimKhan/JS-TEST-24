function range(num1, num2) {
   if ((num1 >= 50 && num1 <= 99) || (num2 >= 50 && num2 <= 99)) {
        return true;
    } else {
        return false;
    }
}

// DOING THE FUNCTION //

console.log(range(45, 60));  
console.log(range(30, 40)); 
console.log(range(60, 95)); 
console.log(range(20, 25)); 

